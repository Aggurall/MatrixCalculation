package model;

public class Point {


    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int _x) {x = _x;}

    public void setY(int _y) {y = _y;}
}
